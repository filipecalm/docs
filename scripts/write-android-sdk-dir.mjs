import fs from "fs";
import os from "os";
import path from "path";

const root = process.cwd();
const androidDir = path.join(root, "android");
const propsPath = path.join(androidDir, "local.properties");

function devLog(...args) {
  if (process.env.NODE_ENV !== "production") {
    console.log(...args);
  }
}

function resolveSdkRoot() {
  const envSdk = (process.env.ANDROID_HOME || process.env.ANDROID_SDK_ROOT || "").trim();
  if (envSdk.startsWith("/mnt/")) {
    console.error(
      "[write-android-sdk-dir] ANDROID_HOME aponta para SDK Windows (/mnt/...). Usa SDK Linux: npm run local:bootstrap"
    );
    process.exit(1);
  }
  const candidates = [];
  if (envSdk) candidates.push(envSdk);
  candidates.push(
    path.join(os.homedir(), "Android", "Sdk"),
    "/opt/android-sdk",
    "/usr/lib/android-sdk"
  );
  for (const c of candidates) {
    if (!c) continue;
    if (c.startsWith("/mnt/")) continue;
    if (fs.existsSync(c) && fs.existsSync(path.join(c, "platform-tools"))) {
      try {
        return fs.realpathSync(c);
      } catch {
        return path.resolve(c);
      }
    }
  }
  console.error(
    "[write-android-sdk-dir] Nenhum Android SDK encontrado. Define ANDROID_HOME ou instala em ~/Android/Sdk (npm run local:bootstrap no WSL)."
  );
  process.exit(1);
}

function formatSdkDirForProperties(abs) {
  if (process.platform === "win32") {
    return abs.replace(/\\/g, "\\\\");
  }
  return abs.replace(/\\/g, "/");
}

const plat = process.env.EAS_BUILD_PLATFORM;
if (plat === "ios") {
  process.exit(0);
}

if (!fs.existsSync(androidDir)) {
  devLog("[write-android-sdk-dir] sem android/; skip.");
  process.exit(0);
}

const sdk = resolveSdkRoot();
if (!fs.existsSync(sdk)) {
  console.error("[write-android-sdk-dir] SDK inexistente:", sdk);
  console.error("WSL/Linux: npm run local:bootstrap  ou  export ANDROID_HOME=$HOME/Android/Sdk");
  process.exit(1);
}

const line = `sdk.dir=${formatSdkDirForProperties(sdk)}\n`;
const header = "# Generated by eas-build-post-install (DietOS)\n";
fs.writeFileSync(propsPath, header + line, "utf8");
devLog("[write-android-sdk-dir]", propsPath, "=>", sdk);
